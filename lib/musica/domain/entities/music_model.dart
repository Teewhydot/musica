class Music {
  final String name;
  final String artist;
  final int duration;
  final String link;
  final bool isPlaying;
  final int noOfTracks;

  Music({
    required this.name,
    required this.artist,
    required this.duration,
    required this.link,
    required this.isPlaying,
    required this.noOfTracks,
  });
}